    {
        if(!Cache::has('all-orders')){
            Cache::put(
                     'all-orders',
                     Order::all()->load('items', 'items.product'),
                     30 //minutes, schedule interval of this very task
                     );
        }
        $orders = Cache::get('all-orders');
        return view('admin.order.list')->with('orders', $orders);
    }

